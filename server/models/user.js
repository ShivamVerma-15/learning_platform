//import mongoose
const mongoose = require('mongoose');

//Schema definition
const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

//giving name to Schema
mongoose.model('User', userSchema);
